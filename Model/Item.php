namespace Black\Bundle\ArticleBundle\Model;

/**
 * Item Model
 */
class Item
{
    /**
     * @var
     */
    protected $name;

    /**
     * Set name
     *
     * @param string $name
     *
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }
}
